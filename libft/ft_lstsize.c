#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		i;
	t_list	*p;

	p = lst;
	i = 0;
	if (p)
	{
		i++;
		while (p->next)
		{
			p = p->next;
			i++;
		}
	}
	return (i);
}
