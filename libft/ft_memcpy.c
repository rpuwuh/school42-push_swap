#include "libft.h"

void	*ft_memcpy(void *restrict dst, const void *restrict src, size_t	n)
{
	size_t	i;

	i = 0;
	if (dst == 0 && src == 0)
		return (0);
	while (i < n)
	{
		*((char *)dst + i) = *((char *)src + i);
		i++;
	}
	return (dst);
}
